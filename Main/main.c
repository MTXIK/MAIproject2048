void map() {
    int arr[4][4] = {{2,  2,    4,  2},
                      {16, 32,   64, 0},
                      {0,  2048, 0,  0,},
                      {0,  128,  0,  0}};
    int i;
    int j;
    printf("_________________________________________\n");// map 2048
    for (i = 0; i <= 3; i++) {
        printf("|         |         |         |         |\n");
//        printf("|");

        for (j = 0; j <= 3; j++) {
            if (arr[i][j] == 0) {
                printf("|    .    ");
            } else if (arr[i][j] == 2 || arr[i][j] == 4 || arr[i][j] == 8) {
                printf("|    %i    ", arr[i][j]);
            } else if (arr[i][j] == 16 || arr[i][j] == 32 || arr[i][j] == 64) {
                printf("|    %i   ", arr[i][j]);
            } else if (arr[i][j] == 128 || arr[i][j] == 256 || arr[i][j] == 512) {
                printf("|   %i   ", arr[i][j]);
            } else if (arr[i][j] == 2048) {
                printf("|   %i  ", arr[i][j]);
            }

        }
        printf("|");
        if (i <= 3) {
            printf("\n");
            printf("|         |         |         |         |\n");
            printf("_________________________________________\n");
        }

    }
}
